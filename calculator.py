def add(x, y):
  answer = x + y
  return answer

def substract(x, y):
  answer = x - y
  return answer

def multiply(x, y):
  answer = x * y
  return answer

def divide(x, y):
  answer = x / y
  return answer

def exp(x, y):
  answer = x ** y
  return answer

print(exp(3, 2))
print(add(3, 2))
print(multiply(3, 2))
print(divide(3, 2))
print(substract(3, 2))